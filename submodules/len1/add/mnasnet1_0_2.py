BS=int(CORES[0])*int(SOCKETS[0])
batch_size=BS
x50 = torch.randn(torch.Size([batch_size, 40, 28, 28]))
x42 = torch.randn(torch.Size([batch_size, 40, 28, 28]))
start_time=time.time()
for i in range(10):
    output = m(x50, x42)
