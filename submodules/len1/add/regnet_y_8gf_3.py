BS=int(CORES[0])*int(SOCKETS[0])
batch_size=BS
x79 = torch.randn(torch.Size([batch_size, 112, 1, 1]))
x93 = torch.randn(torch.Size([batch_size, 448, 28, 28]))
start_time=time.time()
for i in range(10):
    output = m(x79, x93)
