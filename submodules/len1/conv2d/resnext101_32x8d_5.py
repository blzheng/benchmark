class M(torch.nn.Module):
    def __init__(self):
        super(M, self).__init__()
        self.conv2d5 = Conv2d(256, 256, kernel_size=(1, 1), stride=(1, 1), bias=False)

    def forward(self, x16):
        x17=self.conv2d5(x16)
        return x17

m = M().eval()

